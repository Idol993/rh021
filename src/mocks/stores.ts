function generateFinanceRecords(): FinanceRecord[] {
  const records: FinanceRecord[] = []
  const baseIncome = 8000
  const baseExpense = 3500
  for (let i = 29; i >= 0; i--) {
    const d = new Date('2026-06-20')
    d.setDate(d.getDate() - i)
    const dateStr = d.toISOString().split('T')[0]
    const dayOfWeek = d.getDay()
    const isWeekend = dayOfWeek === 0 || dayOfWeek === 6
    const fluctuation = 0.7 + Math.random() * 0.6
    const weekendBoost = isWeekend ? 1.3 : 1.0
    const income = Math.round(baseIncome * fluctuation * weekendBoost)
    const expense = Math.round(baseExpense * (0.8 + Math.random() * 0.4))
    records.push({
      date: dateStr,
      income,
      expense,
      details: [
        { category: '诊疗费', amount: Math.round(income * 0.45) },
        { category: '药品销售', amount: Math.round(income * 0.25) },
        { category: '住院费', amount: Math.round(income * 0.15) },
        { category: '美容寄养', amount: Math.round(income * 0.1) },
        { category: '会员充值', amount: income - Math.round(income * 0.45) - Math.round(income * 0.25) - Math.round(income * 0.15) - Math.round(income * 0.1) },
        { category: '药品采购', amount: Math.round(expense * 0.5) },
        { category: '人员工资', amount: Math.round(expense * 0.3) },
        { category: '设备耗材', amount: expense - Math.round(expense * 0.5) - Math.round(expense * 0.3) },
      ],
    })
  }
  return records
}

